class DashboardWidget extends StatefulWidget {
  const DashboardWidget({super.key});

  static String routeName = 'Dashboard';
  static String routePath = '/dashboard';

  @override
  State<DashboardWidget> createState() => _DashboardWidgetState();
}

class _DashboardWidgetState extends State<DashboardWidget> {
  late DashboardModel _model;

  final scaffoldKey = GlobalKey<ScaffoldState>();

  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => DashboardModel());

    // On page load action.
    SchedulerBinding.instance.addPostFrameCallback((_) async {
      FFAppState().navBarSelection = 1;
      FFAppState().email = '';
      FFAppState().senha = '';
      FFAppState().update(() {});
      _model.aPiTokenAuth = await AuthenticationGroup
          .getTheRecordBelongingToTheAuthenticationTokenCall
          .call(
        bearerAuth: FFAppState().token,
      );

      if ((_model.aPiTokenAuth?.succeeded ?? true)) {
        FFAppState().isMap = false;
        FFAppState().update(() {});
        if ((AuthenticationGroup
                    .getTheRecordBelongingToTheAuthenticationTokenCall
                    .paymentID(
                  (_model.aPiTokenAuth?.jsonBody ?? ''),
                ) ==
                3) ||
            (AuthenticationGroup
                    .getTheRecordBelongingToTheAuthenticationTokenCall
                    .paymentID(
                  (_model.aPiTokenAuth?.jsonBody ?? ''),
                ) ==
                4)) {
          context.goNamedAuth(
            ExpiredplanWidget.routeName,
            context.mounted,
            extra: <String, dynamic>{
              kTransitionInfoKey: TransitionInfo(
                hasTransition: true,
                transitionType: PageTransitionType.fade,
                duration: Duration(milliseconds: 0),
              ),
            },
          );

          return;
        } else if (AuthenticationGroup
                .getTheRecordBelongingToTheAuthenticationTokenCall
                .paymentID(
              (_model.aPiTokenAuth?.jsonBody ?? ''),
            ) ==
            5) {
          context.goNamedAuth(
            PagePriceWidget.routeName,
            context.mounted,
            queryParameters: {
              'companyID': serializeParam(
                AuthenticationGroup
                    .getTheRecordBelongingToTheAuthenticationTokenCall
                    .companyID(
                  (_model.aPiTokenAuth?.jsonBody ?? ''),
                ),
                ParamType.int,
              ),
            }.withoutNulls,
            extra: <String, dynamic>{
              kTransitionInfoKey: TransitionInfo(
                hasTransition: true,
                transitionType: PageTransitionType.fade,
                duration: Duration(milliseconds: 0),
              ),
            },
          );

          return;
        } else {
          return;
        }
      } else {
        GoRouter.of(context).prepareAuthEvent();
        await authManager.signOut();
        GoRouter.of(context).clearRedirectLocation();

        return;
      }
    });

    WidgetsBinding.instance.addPostFrameCallback((_) => safeSetState(() {}));
  }

  @override
  void dispose() {
    _model.dispose();

    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    context.watch<FFAppState>();

    return GestureDetector(
      onTap: () {
        FocusScope.of(context).unfocus();
        FocusManager.instance.primaryFocus?.unfocus();
      },
      child: Scaffold(
        key: scaffoldKey,
        backgroundColor: FlutterFlowTheme.of(context).primaryBackground,
        body: SafeArea(
          top: true,
          child: Row(
